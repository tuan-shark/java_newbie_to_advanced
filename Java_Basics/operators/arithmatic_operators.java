package Java_Basics.operators;

public class arithmatic_operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
    }
}
